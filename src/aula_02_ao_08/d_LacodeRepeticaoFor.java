package aula_02_ao_08;//  for (Laço de Repetição):

public class d_LacodeRepeticaoFor {
    public static void main(String[] args) {
        // Imprime números de 0 a 4
        for (int i = 0; i < 5; i++) {
            System.out.println("Número: " + i);
        }

        // Exemplo com decremento
        for (int i = 5; i > 0; i--) {
            System.out.println("Contagem regressiva: " + i);
        }
    }
}


