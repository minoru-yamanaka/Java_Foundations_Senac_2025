package aula_02_ao_08;// Estruturas de Controle de Fluxo

// Permitem que você controle a ordem de execução do seu código.
// if/else if/else (Condicionais):
public class d_Condicionais {
    public static void main(String[] args) {
        int hora = 15;

        if (hora < 12) {
            System.out.println("Bom dia!");
        } else if (hora < 18) {
            System.out.println("Boa tarde!");
        } else {
            System.out.println("Boa noite!");
        }
    }
}



