package atividades;

public class Atividade01 {

    public static void main(String[] args) {

        // --- Atividade 1: Olá Mundo e Variáveis ---
        System.out.println("--- Atividade 1: Olá Mundo e Variáveis ---");
        // Imprime a mensagem "Olá, Mundo do Java!" no console.
        System.out.println("Olá, Mundo do Java!");

        // Declara uma variável String chamada nome e atribui seu nome a ela.
        String nome = "Seu Nome"; // Substitua "Seu Nome" pelo seu nome
        // Declara uma variável int chamada idade e atribui sua idade a ela.
        int idade = 30; // Substitua 30 pela sua idade
        // Declara uma variável double chamada altura e atribui sua altura a ela.
        double altura = 1.75; // Substitua 1.75 pela sua altura

        // Imprime uma frase combinando essas variáveis.
        System.out.println("Olá, meu nome é " + nome + ", tenho " + idade + " anos e " + altura + " metros de altura.");
        System.out.println("\n"); // Adiciona uma linha em branco para melhor legibilidade
    }
}